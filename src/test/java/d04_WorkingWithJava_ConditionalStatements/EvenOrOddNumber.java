package d04_WorkingWithJava_ConditionalStatements;

public class EvenOrOddNumber {

	//Example 2: even or Odd
		
		public static void main(String[] args) {
			
			int num=15;
			
			if(num%2==0)
			{
				System.out.println("Even Number");
			}
			else
			{
				System.out.println("Odd Number");
			}
		}
}

